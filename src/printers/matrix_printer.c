void	print_mat(t_mat44 *mat)
{
	printf("%f %f %f %f\n", mat->a[0], mat->b[0], mat->c[0], mat->d[0]);
	printf("%f %f %f %f\n", mat->a[1], mat->b[1], mat->c[1], mat->d[1]);
	printf("%f %f %f %f\n", mat->a[2], mat->b[2], mat->c[2], mat->d[2]);
	printf("0 0 0 1\n");
}
